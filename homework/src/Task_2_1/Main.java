package Task_2_1;

public class Main {
    public static void main(String[] args) {
         result(10,5);
    }
    static void result(int x, int b){
        System.out.println(x + b);
        System.out.println(x - b);
        System.out.println(x * b);
        System.out.println(x / b);
        System.out.println(Math.abs(x) + b);
        System.out.println(Math.sqrt(b));
    }
}
